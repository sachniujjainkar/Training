
#Ruby Metaprogramming: Code Writing Code

=begin
Metaprogramming is a technique by which you can write code that writes code by itself dynamically at runtime. 
This means you can define methods and classes during runtime.

DRY(Don,t repeat yourself)
don't repeat yourself (DRY) is a principle of software development aimed at reducing repetition of software patterns, 
replacing them with abstractions; and several copies of the same data, 
using data normalization to avoid redundancy.
=end